#include <XMA/Core/Timer.hpp>

namespace XMA { namespace Core {

// ---------------------------------------------------------------------------------------------------------------------

Timer::Timer()
{
    reset();
}

// ---------------------------------------------------------------------------------------------------------------------

Timer::~Timer()
{}

// ---------------------------------------------------------------------------------------------------------------------

Timer* Timer::reset()
{
    m_start = SDL_GetPerformanceCounter();
    return this;
}

// ---------------------------------------------------------------------------------------------------------------------

float Timer::getSeconds() const
{
    return (float)((double) ((SDL_GetPerformanceCounter() - m_start) * 1000 / SDL_GetPerformanceFrequency())) / 1000.0;
}


// ---------------------------------------------------------------------------------------------------------------------

}}