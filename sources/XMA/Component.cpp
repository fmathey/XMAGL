#include <XMA/Component.hpp>
#include <XMA/Entity.hpp>

namespace XMA {

// ---------------------------------------------------------------------------------------------------------------------

Entity& Component::getEntity()
{
    return *m_entity;
}

// ---------------------------------------------------------------------------------------------------------------------

Display Component::getDisplay() { return Display(m_engine->display); }

// ---------------------------------------------------------------------------------------------------------------------

Input Component::getInput() { return Input(m_engine->input); }

// ---------------------------------------------------------------------------------------------------------------------

float Component::getElapsedTime() const { return m_engine->elapsedTime; }

// ---------------------------------------------------------------------------------------------------------------------

float Component::getFrameTime() const { return m_engine->frameTime; }

// ---------------------------------------------------------------------------------------------------------------------

float Component::getDeltaTime() const { return m_engine->deltaTime; }

// ---------------------------------------------------------------------------------------------------------------------

float Component::getFramesPerSeconds() const { return m_engine->framesPerSeconds; }

// ---------------------------------------------------------------------------------------------------------------------

float Component::getTimescale() const { return m_engine->timeScale; }

// ---------------------------------------------------------------------------------------------------------------------

Component& Component::setTimescale(float scale) { m_engine->timeScale = scale; return *this; }

// ---------------------------------------------------------------------------------------------------------------------

}